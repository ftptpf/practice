package ru.practice.dive;

public class DooBee {
    public static void main(String[] args) {
        int x = 1;
        while(x < 4) {
            System.out.println("Doo");
            System.out.println("Bee");
            x++;
        }
        if(x == 4) {
            System.out.println("Do");
        }
    }
}
